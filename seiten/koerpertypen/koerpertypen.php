
<h1>Körper- und Persönlichkeitstypologie</h1>
      <p>Zu jedem körperlichen Erscheinungsbild gehören auch bestimmte Ängste, Stärken, Vorlieben und Gewohnheiten. Die fünf Körpertypen - sonst trockener Stoff im Fachjargon der Bioenergetik - werden hier mit Witz und Verstand in Szene gesetzt. Mit diese Wissen kann man erstaunlich genau vorhersagen, wie sich andere in bestimmten Situationen verhalten - vor allem aber die eigenen Verhaltensmuster durchschauen, verstehen und darüber hinauswachsen. </p>
      <p>Die Persönlichkeit eines Menschen wird von seiner spezifischen inneren Vitalität bestimmt, das heisst von der Stärke seiner Impulse und von den Abwehrhaltungen, die er errichtet hat, um diese Impulse zu steuern. Jeder Mensch hat aus seinen persönlichen Lebenserfahrungen sein spezifisches Abwehrmuster entwickelt, welches sich in der körperlichen Erscheinung, im energetischen Zustand und im Wesen - in der 'Persönlichkeit' - ausdrückt. </p>
